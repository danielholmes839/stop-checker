export const FavouriteIcon: React.FC<{ placeId: string }> = ({ placeId }) => {
  const { getFavourite } = useStorage();
  let fav = getFavourite(placeId);
  return <FavouriteIconByName icon={fav ? fav.icon : "saved"} />;
};

export const FavouriteIconByName: React.FC<{
  icon: FavouriteIconName;
}> = ({ icon }) => {
  if (icon === "saved") {
    return <SavedIcon />;
  } else if (icon === "home") {
    return <HomeIcon />;
  } else if (icon === "office") {
    return <OfficeIcon />;
  } else if (icon === "school") {
    return <SchoolIcon />;
  }
  return <SavedIcon />;
};

export const SearchLocationIcon: React.FC<{ placeId: string }> = ({
  placeId,
}) => {
  const { isFavourite, isHistory } = useStorage();
  if (isFavourite(placeId)) {
    return <FavouriteIcon placeId={placeId} />;
  } else if (isHistory(placeId)) {
    return <HistoryIcon />;
  }
  return <MarkerIcon />;
};

export const PlaceIcon: React.FC<{ placeId: string | null }> = ({
  placeId,
}) => {
  const { isFavourite } = useStorage();
  if (placeId && isFavourite(placeId)) {
    return <FavouriteIcon placeId={placeId} />;
  }
  return <MarkerIcon />;
};
